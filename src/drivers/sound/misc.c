short mulaw_expand(unsigned char data)
{
	static const unsigned short exp_tab[8] =
	{
		0, 132, 396, 924, 1980, 4092, 8316, 16764
	};
	unsigned char exp;
	short ret_val;

	data = ~data;
	exp = (data >> 4) & 7;
	ret_val = exp_tab[exp] + ((data & 0x0F) << (exp + 3));
	if((data & 0x80) != 0)
		ret_val = -ret_val;
	return ret_val;
}
