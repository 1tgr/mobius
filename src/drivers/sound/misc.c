/*****************************************************************************
this function assumes sizeof(long)==4
and it assumes little-endian CPU (e.g. x86)
*****************************************************************************/
unsigned long read_be32(unsigned char *buffer)
{/* 'volatile' makes code a little smaller with GCC for x86 */
	volatile union
	{
		unsigned long dword;
		unsigned char byte[4];
	} temp;

	temp.byte[0] = buffer[3];
	temp.byte[1] = buffer[2];
	temp.byte[2] = buffer[1];
	temp.byte[3] = buffer[0];
	return temp.dword;
}
/*****************************************************************************
this function assumes sizeof(short)==2
and it assumes little-endian CPU (e.g. x86)
*****************************************************************************/
unsigned short read_be16(unsigned char *buffer)
{
	union
	{
		unsigned long word;
		unsigned char byte[2];
	} temp;

	temp.byte[0] = buffer[1];
	temp.byte[1] = buffer[0];
	return temp.word;
}
