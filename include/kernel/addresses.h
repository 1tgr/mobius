#ifndef __KERNEL_ADDRESSES_H
#define __KERNEL_ADDRESSES_H

#define ADDR_LOW_END_PHYS	0x00100000
#define ADDR_KERNEL_PHYS	0x00100000

#define ADDR_KERNEL_VIRT	0xC0000000
#define ADDR_PAGES_VIRT		0xD0000000
#define ADDR_HEAP_VIRT		0xE0000000
#define ADDR_TEMP_VIRT		0xF0000000
#define ADDR_PAGETABLE_VIRT 0xFFC00000
#define ADDR_PAGEDIRECTORY_VIRT \
	(ADDR_PAGETABLE_VIRT + (ADDR_PAGETABLE_VIRT / (1024)))

#define KERNEL_FLAT_CODE    0x08
#define KERNEL_FLAT_DATA    0x10
#define USER_FLAT_CODE      0x18
#define USER_FLAT_DATA      0x20
#define KERNEL_TSS          0x28
#define USER_THREAD_INFO    0x30
#define KERNEL_DF_TSS       0x38

#endif
